static unsigned char *setup_phys_array(void *src, int len)
{
#ifdef ARC_ENCODER_API
    unsigned char *phys;
    void *virt = arc_encoder_api_malloc(len, ENC_MEM_OPSTREAM|ENC_MEM_DMA_SRC|ENC_MEM_ALIGN_8, &phys, 0);

    memcpy(virt, src, len);
    return phys;
#else
    return src;
#endif
}

void aanscaletable_init(void)
{
    p_aanscaletables = (void *)setup_phys_array(&src_aanscaletables, sizeof(src_aanscaletables));
    p_aanscaletables263 = (void *)setup_phys_array(&src_aanscaletables263, sizeof(src_aanscaletables263));
}

void aanscaletable_uninit(void)
{
#ifdef ARC_ENCODER_API
    arc_encoder_api_free(p_aanscaletables, ENC_MEM_OPSTREAM, 0);
    arc_encoder_api_free(p_aanscaletables263, ENC_MEM_OPSTREAM, 0);
#endif
}

